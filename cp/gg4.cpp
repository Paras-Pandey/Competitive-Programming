long Anthill(vector<int> A, int k) {
    int n = A.size();
    int dp[k+1];
    memset(dp, 0, sizeof dp);
 
    for (int i=0; i<=k; i++)
      for (int j=0; j<n; j++)
         if (j + 1 <= i)
            dp[i] = max(dp[i], dp[i-j-1] + A[j]);
 
    return dp[k];
}

